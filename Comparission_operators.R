# we are performing comparison operators in R

# The comparison operators are used to compare two values. The comparison operators are as follows:
# 1. == : equal to
# 2. != : not equal to
# 3. > : greater than
# 4. < : less than
# 5. >= : greater than or equal to
# 6. <= : less than or equal to

