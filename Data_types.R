# The data types are 5 types in R. They are as follows:
# 1. Numeric    
# 2. Character
# 3. Logical
# 4. Complex
# 5. integer :in it (eg 1L, 2L, 3L) where L is used to denote integer values


# lets Practice the data types in R

#numeric data type
num1 <- 10.5

#character data type
char1 <- "Hello"

#logical data type
log1 <- TRUE

#complex data type
comp1 <- 2 + 3i

#integer data type
int1 <- 5L

# we can check the data type of a variable using the class() function

class(num1)

class(char1)

class(log1)

class(comp1)

class(int1)