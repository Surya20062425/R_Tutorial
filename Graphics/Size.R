# Use cex=number to change the size of the points

# 1 is default, while 0.5 means 50% smaller, and 2 means 100% larger

plot(1:11,cex=2)

# maximum size 

# we can create a line 

plot(1:10, type="l", cex=2)

# we have created a large line

# Now we are adding the color to the line

plot(1:10,type="l",cex=2,col="red")

# we have created a large line and added  red