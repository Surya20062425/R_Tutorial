# Use pch with a value from 0 to 25 to change the point shape format
# pch values  are 0-25
plot(1:10,pch=0,cex=2) #pch =0 is Open square
plot(1:10,pch=1,cex=2) #pch =0 is Open open circle
plot(1:10,pch=2,cex=2) #pch =0 is Open triangle
plot(1:10,pch=3,cex=2) #pch =0 is plus sign
plot(1:10,pch=4,cex=2) #pch =0 is cross
plot(1:10,pch=5,cex=2) #pch =0 is open diamond
plot(1:10,pch=6,cex=2) #pch =0 is Open triangle
