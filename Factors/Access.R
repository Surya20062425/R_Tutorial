#  Creating a Factor 

# Factor is same like a list 

mine <- factor("you","and ","me", "is ","us")

# created a factor mine


# We can access the elements present in the factor


mine[2] # By this we can access the 2 element in the mine

# we have to know the index for access the element

# we can access multiple elements at a time

mine[c(2,4)]

# In above step we have accessed two elements at a time

