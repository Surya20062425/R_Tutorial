 # we are using the boolean data type in R. The boolean data type has two values TRUE and FALSE. We can use the boolean data type to perform logical operations in R.
 # we can use the boolean data type to check if a condition is true or false. The boolean data type is used in conditional statements and loops in R.
 # for example, we can use the boolean data type to check if a number is greater than another number. If the condition is true, it will return TRUE, otherwise it will return FALSE.
 # we can also use the boolean data type to check if a string is equal to another string
a <- 10
b <- 9

a > b
 # now we are using if else statement to check if a number is greater than another number. If the condition is true, it will print "a is greater than b", otherwise it will print "a is not greater than b".
if (a > b) {
  print("a is greater than b")  
} else {
  print("a is not greater than b")
}